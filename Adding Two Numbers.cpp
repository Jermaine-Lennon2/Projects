#include <iostream>
using namespace std;

int main(){
    int num1, num2, answ;
    
    cout << "Enter Two Numbers: " << endl;
    cin >> num1 >> num2;
    
    answ = num1 + num2;
    
    cout << num1 << " + " << num2 << " = " << answ << endl;
    
    return 0;
    
}
