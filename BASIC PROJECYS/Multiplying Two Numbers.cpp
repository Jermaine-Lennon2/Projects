#include <iostream>
using namespace std;


int main()
{
    double x, y, answ;
    
    cout << "ENTER FIRT NUMBER: " << endl;
    cin >> x;
    
    cout << "ENTER SECOND NUMBER: " << endl;
    cin >> y;
    
    answ = x * y;
    
    cout << x << " * " << y << " = " << answ << endl;
    
    return 0;
    
}
